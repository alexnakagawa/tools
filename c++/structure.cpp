// This is called a preprocessor directive. <iostream> is part of the "C++ Standard Library"
// It is used for writing to the screen and getting input from the user
#include <iostream>
 
 // This is function declaration. Every program must have a main function.
int main()
{
	// << is the output operator
	// std::cout represents the console/screen
	// Since this is a statement, we end it with a semicolon (;)
	std::cout << "Hello world!";
	return 0;
}



